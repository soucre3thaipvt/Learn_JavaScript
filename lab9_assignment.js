// Arithmetic Operators: Toán tử trong js
// 1. ++  --
// 2. *   /   %
// 3. +   -
// 4. Phep gan: = += -= *= /= %=

var a;
var b;
// gán nhiều biến vào 1 giá trị
a = b = 1;
// a = b; b = 1;

a += 5;
// a = a + 5

a -= 4;
// a = a - 4

a *= 2;
// a = a * 2

a /= 5;
// a = a / 5

a %= 3;
// a = a % 3

a *= b / 2;
// a = a * b / 2



